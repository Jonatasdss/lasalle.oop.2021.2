package TrabalhoRefatoracao;

public class Time {
    
    private String nome;
	private String escudo;

	public Time() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEscudo() {
		return escudo;
	}

	public void setEscudo(String escudo) {
		this.escudo = escudo;
	}
}
